            <div class="collapsible-header"><h5><i class="material-icons">desktop_windows</i>What is EPR?</h5></div>
            <div class="collapsible-body">
              <p class="flow-text" style="font-weight: 600;">EPR is a single patient record that will be accessible across all our hospital sites and will replace paper records over the next 12 months. It combines the best of what current systems do – and more – improving communication, documentation and care.</p>
              <p class="flow-text">Staff will be able to enter data
and documents straight into EPR. Integrated medical devices, such as blood pressure monitors, will upload automatically to the patient’s record, reducing potential errors and freeing up clinicians’ time. If a patient’s observations and assessments are outside expected ranges, staff will be alerted and prompted with appropriate care plans to give the best treatment.</p>
              <p class="flow-text">EPR will provide an accurate and real-time view of each patient’s care, using live data to create dynamic documents for clinical records and correspondence to GPs and patients.</p>
              <p class="flow-text">Your smartcard will provide appropriate role-based access to
the EPR. Multiple staff can view and document the same patient record simultaneously. Once you are logged into EPR, you will remain logged in for the rest of the day, with no need to remember multiple passwords for different systems.</p>
              <p class="flow-text">EPR means we will no longer have paper records at the patient’s bedside. Instead, there will be computers or laptops which can be used to input information directly. In the future, staff will be able to use an app on their phone, or a microphone, to enter data using speech recognition technology.</p>
              <p class="flow-text">EPR is being underpinned with new IT infrastructure including new PCs. It is supported with funding from the national Global Digital Exemplar (GDE) programme. The RFL was one of 16 digitally advanced acute trusts chosen by NHS England to pioneer IT projects and received £10m, matched by the trust, bringing total funds to £20m.</p>
